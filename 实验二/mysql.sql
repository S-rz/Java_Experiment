/*科室信息表*/
DROP TABLE T_KSXX;

CREATE TABLE T_KSXX(
   KSBH CHAR(6) NOT NULL,
   KSMC CHAR(10) NOT NULL,
   PYZS CHAR(8) NOT NULL,

   PRIMARY KEY (KSBH),
   INDEX (KSBH(6))
)ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

/*病人信息表*/
DROP TABLE T_BRXX;

CREATE TABLE T_BRXX(
   BRBH CHAR(6) NOT NULL,
   BRMC CHAR(10) NOT NULL,
   DLKL CHAR(8) NOT NULL,
   YCJE DECIMAL(10,2) NOT NULL,
   DLRQ DATETIME,

   PRIMARY KEY (BRBH),
   INDEX (BRBH(6))
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

/*科室医生表*/
DROP TABLE T_KSYS;

CREATE TABLE T_KSYS(
   YSBH CHAR(6) NOT NULL,
   KSBH CHAR(6) NOT NULL,
   YSMC CHAR(10) NOT NULL,
   PYZS CHAR(4) NOT NULL,
   DLKL CHAR(8) NOT NULL,
   SFZJ TINYINT NOT NULL,
   DLRQ DATETIME,

   PRIMARY KEY (YSBH),
   INDEX (YSBH(6)),
   INDEX (KSBH(6))
)ENGINE=InnoDB DEFAULT CHARSET=utf8;

/*号种信息表*/
DROP TABLE T_HZXX;

CREATE TABLE T_HZXX(
   HZBH CHAR(6) NOT NULL,
   HZMC CHAR(12) NOT NULL,
   PYZS CHAR(8) NOT NULL,
   KSBH CHAR(6) NOT NULL,
   SFZJ TINYINT NOT NULL,
   GHRS INT NOT NULL,
   GHFY DECIMAL(8,2) NOT NULL,

   PRIMARY KEY (HZBH),
   INDEX (HZBH(6)),
   INDEX (KSBH(6))
)ENGINE=InnoDB DEFAULT CHARSET=utf8;


/*挂号信息表*/
DROP TABLE T_GHXX;

CREATE TABLE T_GHXX(
   GHBH CHAR(6) NOT NULL,
   HZBH CHAR(6) NOT NULL,
   YSBH CHAR(6) NOT NULL,
   BRBH CHAR(6) NOT NULL,
   GHRC INT NOT NULL,
   THBZ TINYINT NOT NULL,
   GHFY DECIMAL(8,2) NOT NULL,
   RQSJ DATETIME NOT NULL,
   KBSJ DATETIME,
   PRIMARY KEY (GHBH),
   INDEX (GHBH(6)),
   INDEX (HZBH(6)),
   INDEX (YSBH(6)),
   INDEX (BRBH(6)),
   INDEX (GHRC)
)ENGINE=InnoDB DEFAULT CHARSET=utf8;
